/**
 * Gets the size of an ASCII `string`.
 *
 * @private
 * @function
 * @since v0.1.0
 * @category lang.util
 * @param {string} ascii The ascii string to inspect.
 * @returns {number} Returns the string size.
 * @example
 *
 * asciiSize('foo')
 * //=> 3
 */
const asciiSize = (ascii) => ascii.length

export default asciiSize
